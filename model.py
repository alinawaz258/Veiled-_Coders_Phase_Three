"""GigShield AI — ML Model Management (v3.0.0).

Why this is a genuine ML model, not a rule engine
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
The v1/v2 synthetic data was generated by a linear formula:
    target = 0.35*weather + 0.20*pollution + ...
This meant the GBM was approximating a weighted sum — effectively a rule engine.

v3.0 fixes this with four structural changes:

1. NON-LINEAR SYNTHETIC DATA
   The target is generated with multiplicative interactions, power-law terms,
   threshold step-functions, and seasonal amplifiers. A linear model cannot
   fit this surface; only a tree-based learner can capture it.

2. ENGINEERED INTERACTION FEATURES
   Six new features expose the non-linearities to the GBM explicitly:
   - rain_flood_interaction   (compound: multiplicative, not additive)
   - heat_aqi_synergy         (outdoor pollution worse in extreme heat)
   - compound_severity        (triple-factor compound risk)
   - heavy_rain_flag          (parametric threshold at 50 mm/hr)
   - severe_aqi_flag          (CPCB Severe threshold at AQI 300)
   - monsoon_flag             (binary seasonal context)

3. ML AS PRIMARY SIGNAL
   The GBM output provides the BASE probability used for:
   - risk_level classification
   - plan recommendation
   - premium calculation
   A post-model fraud adjustment may be applied in risk_engine/app,
   but fraud is NOT used as an ML feature.
   The heuristic calculate_explainability_score() in risk_engine.py
   exists ONLY for rider-facing explanations and IRDAI audit trail.

4. QUANTITATIVE PROOF OF NON-LINEARITY
   model_meta.json includes 'linear_baseline_r2': the R² of a LinearRegression
   fit on the same data. The gap (GBM R² - Linear R²) proves the GBM is
   capturing variance that no linear formula can reach.

Seasonal calibration
~~~~~~~~~~~~~~~~~~~~
The synthetic data includes a monsoon_flag feature (35% of samples are
monsoon-week scenarios). This makes the model aware of seasonal context —
during monsoon weeks, compound flooding and rainfall have non-linearly
higher impact on disruption probability.
"""

